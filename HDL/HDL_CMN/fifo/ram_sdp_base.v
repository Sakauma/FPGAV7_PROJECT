`timescale 1ns / 1ps

module ram_sdp_base #
(//sdp : Simple Dual Port
    parameter ADDR_WIDTH        =      9,
    parameter DATA_WIDTH        =     18,
    parameter REG_OUT           =      1,//0<=REG_OUT<=2. It's highly recommended to set this parama bigger than '0'. Otherwise, synthieser will use logic resource to build the RAM unless the value of MEM_STYLE.
    parameter MEM_STYLE         = "AUTO",//For Xilinx : use "AUTO", "BLOCK", or "DISTRIBUTED" ; For Altera : use "logic", "M512", "M4K", "M9K", "M20K", "M144K", "MLAB", or "M-RAM".
    parameter USE_RD_EN         =      0,//If USE_RD_EN = 1, user should drive I_rd_en port as read enable. Otherwise, this port is unused, left it un-conected or assign it with any value.
    parameter COL_NUM           =      1,//Number of byte write enable bit
    
    //auto generated by the parama above, no need to allocate
    parameter FULL_DATA_WIDTH  = DATA_WIDTH*COL_NUM
)
(
    input                        I_wr_clk ,
    input  [COL_NUM-1:0]         I_wr_en  ,
    input  [ADDR_WIDTH-1:0]      I_wr_addr,
    input  [FULL_DATA_WIDTH-1:0] I_wr_data,
    input                        I_rd_clk ,
    input                        I_rd_en  ,
    input  [ADDR_WIDTH-1:0]      I_rd_addr,
    output [FULL_DATA_WIDTH-1:0] O_rd_data
);
    
    function integer div_round
    (
        input integer a,
        input integer b
    );
    begin
        div_round = (a/b) + ((a%b)!=0);
    end
    endfunction
    
    localparam ALTERA_RAM = (MEM_STYLE=="M20K")||(MEM_STYLE=="M9K");
    localparam ALTERA_SYNCRAM_FORCE = (COL_NUM>1 && ALTERA_RAM);
    
    genvar i,j,k;
    generate
        
        if(ALTERA_SYNCRAM_FORCE)//Altera RAMЕФByte WriteЙІФмФПЧАЭЈЙ§ЕїIPЪЕЯжЁЃЪжаДднВЛжЇГж
            begin: syncram_force
                
                //ДЫЗжжЇдкдЫгУЩЯЕФЯожЦ:
                //1. 1<=REG_OUT<=2
                //2. жЇГжByte WriteЪБ(МДCOL_NUM>1)ЃЌDATA_WIDTHашЮЊ8Лђ9Лђ10ЕФећЪ§БЖ
                //3. MEM_STYLE=="SYNCRAM_FORCE"ЧвCOL_NUM==1ЪБЃЌПЩФмЛсв§Ц№зЪдДРЫЗбЁЃ
                //   БШШчDATA_WIDTH==35ЪБЃЌФкВПМЦЫуBYTE_SIZEЮЊ8ЃЌЪЕдђУЛга9ЛЎЫуЁЃ
                //   ДЫЪБНЈвщдЫгУЩЯНЋ35ИФГЩ36ЃЌЛђепаоИФMEM_STYLEзпЦфЫќЗжжЇ
                
                ram_sdp_base_altera #
                (
                    .ADDR_WIDTH(ADDR_WIDTH),
                    .DATA_WIDTH(DATA_WIDTH),
                    .REG_OUT   (REG_OUT   ),//1<=REG_OUT<=2
                    .MEM_STYLE (MEM_STYLE ),//use "logic", "M512", "M4K", "M9K", "M20K", "M144K", "MLAB", or "M-RAM".
                    .USE_RD_EN (USE_RD_EN ),//If USE_RD_EN = 1, user should drive I_rd_en port as read enable. Otherwise, this port is unused, left it un-conected or assign it with any value.
                    .COL_NUM   (COL_NUM   ) //Number of byte write enable bit
                )
                u_ram_sdp_base_altera
                (
                    .I_wr_clk (I_wr_clk ),
                    .I_wr_en  (I_wr_en  ),
                    .I_wr_addr(I_wr_addr),
                    .I_wr_data(I_wr_data),
                    .I_rd_clk (I_rd_clk ),
                    .I_rd_en  (I_rd_en  ),
                    .I_rd_addr(I_rd_addr),
                    .O_rd_data(O_rd_data)
                );
                
            end
    
        else if(MEM_STYLE=="URAM_FORCE")
            begin : uram_force
                
                //ДЫЗжжЇдкдЫгУЩЯЕФЯожЦ:
                //1. 1<=REG_OUT<=2
                //2. жЇГжByte WriteЪБ(МДCOL_NUM>1)ЃЌDATA_WIDTHашЮЊ8Лђ9ЕФећЪ§БЖ
                //3. ЖСаДЪБжгашЮЊЭЌвЛИі
                //4. URAM288ЕФЩюЖШЮЊ1024ЃЌПэЖШ72bitЃЌВЛПЩЕїЁЃ
                //   ЩюЖШКЭПэЖШГЌЗЖЮЇЪБЃЌашвЊдкЭтВуМгТпМ­ЪЕЯжЖрИіURAMЕФЦДНгЃЈЯТУцвбЪЕЯжЃЉЁЃ
                
                localparam M = (ADDR_WIDTH>12) ? (ADDR_WIDTH-12) : 0;//гУ2^MИіURAMзнЯђЦДНгЃЌдіМгЩюЖШ
                localparam N = div_round(COL_NUM*DATA_WIDTH,72);//гУNИіURAMКсЯђЦДНгЃЌдіМгПэЖШ
                
                localparam DATA_WIDTH_S = (DATA_WIDTH%9==0) ? 9 : 8;
                localparam COL_NUM_S = 72/DATA_WIDTH_S;
                localparam REG_OUT_S = (REG_OUT==2&&M>0) ? 1 : REG_OUT;
                
                wire [M+11:0] wr_addr;
                wire [M+11:0] rd_addr;
                wire [N*72-1:0] wr_data           ;
                wire [N*72-1:0] rd_data [2**M-1:0];
                
                //ЖўепЮЛПэВЛвЛжТЪБЃЌИпЮЛздЖЏВЙ0
                assign wr_addr   = I_wr_addr;
                assign rd_addr   = I_rd_addr;
                assign wr_data   = I_wr_data;
                
                localparam W = div_round(DATA_WIDTH,DATA_WIDTH_S);//1ИіI_wr_enЕФbitПЩЖдгІWИіbyte_wr_enЕФbit
                wire [N*COL_NUM_S-1:0] byte_wr_en;
                for(k=0;k<N*COL_NUM_S;k=k+1)                 //етЖЮДњТыПЩФмВЛвзРэНт
                begin:bwr                                    //ЦфаЇЙћЪЧЃК
                    if((k/W)<COL_NUM)                        //НЋI_wr_enЕФУПИіbitРЉеЙWЗнЃЌ
                        assign byte_wr_en[k] = I_wr_en[k/W];//зїЮЊURAMЕФаДзжНкЪЙФмЃЌ
                    else                                    //
                        assign byte_wr_en[k] = 1'b0;        //РЉеЙКѓЮЛПэВЛЙЛЕФЃЌИпЮЛВЙ0
                end
                
                if(M==0)
                    begin:m0
                        assign O_rd_data = rd_data[0];
                    end
                else 
                    begin:mx
                        
                        reg  [M+11:12] rd_addr_h;
                        always @ (posedge I_rd_clk)
                        begin
                            if(!USE_RD_EN)
                                rd_addr_h <= wr_addr[M+11:12];
                            else if(I_rd_en)
                                rd_addr_h <= wr_addr[M+11:12];
                        end
                        
                        if(REG_OUT==2)
                            begin:r2
                                reg  [N*72-1:0] rd_data_s;
                                always @ (posedge I_rd_clk)
                                begin
                                    rd_data_s = rd_data[rd_addr_h];
                                end
                                assign O_rd_data = rd_data_s;
                            end
                        else
                            begin:r1//ДЫЗжжЇЕФЪБађЩдВю
                                assign O_rd_data = rd_data[rd_addr_h];
                            end
                    end
                
                wire [2**M-1:0] wr_cs;
                
                for(i=0;i<2**M;i=i+1)
                begin:depth
                    
                    if(M==0)
                        begin:m0
                            assign wr_cs[i] = 1'b1;
                        end
                    else
                        begin:mx
                            assign wr_cs[i] = (wr_addr[M+11:12]==i);
                        end
                    
                    for(j=0;j<N;j=j+1)
                    begin:width
                        
                        ram_sdp_base_uram #
                        (
                            .DATA_WIDTH(DATA_WIDTH_S),//DATA_WIDTH*COL_NUM should be less or equal to 72,and DATA_WIDTH/8 or DATA_WIDTH/9 is an interger
                            .REG_OUT   (REG_OUT_S   ),//1<=REG_OUT<=2
                            .USE_RD_EN (USE_RD_EN   ),//If USE_RD_EN = 1, user should drive I_rd_en port as read enable. Otherwise, this port is unused, left it un-conected or assign it with any value.
                            .COL_NUM   (COL_NUM_S   ) //Number of byte write enable bit
                        )
                        u_ram_sdp_base_uram
                        (
                            .I_wr_clk (I_wr_clk                     ),
                            .I_wr_en  (byte_wr_en[j*COL_NUM_S+:COL_NUM_S]&{COL_NUM_S{wr_cs[i]}}),
                            .I_wr_addr(wr_addr[11:0]                ),
                            .I_wr_data(wr_data[72*j+:72]            ),
                            .I_rd_clk (I_rd_clk                     ),
                            .I_rd_en  (I_rd_en                      ),
                            .I_rd_addr(rd_addr[11:0]                ),
                            .O_rd_data(rd_data[i][72*j+:72]         )
                        );
                        
                    end
                end
                
                
            end
    
        else
            
            begin : common
                
                //ДЫЗжжЇдкдЫгУЩЯЕФЯожЦ:
                //1. ШчЙћMEM_STYLEВЩгУXilinxЛђепAlteraЕФзЈгУRAMзЪдДЃЌ1<=REG_OUT<=2ЁЃ
                //   ШчЙћгУТпМ­зЪдДДюНЈRAMЃЌREG_OUTПЩвдЮЊ0ЁЃ
                //2. AltaraзЈгУRAMЮоЗЈЭЈЙ§ДЫЗжжЇЪЕЯжByte WriteЃЌЪЙгУЪБЧыДЋЕне§ШЗЕФMEM_STYLEВЮЪ§ЁЃ
                //   ЗёдђПЩФмЛсВЩгУТпМ­зЪдДЪЕЯжЃЌЛђепЕМжТЪЙгУИќЖрЕФM20K/M9KЁЃ
                //3. Xilinx BRAM/URAMжЇГжByte WriteЪБ(МДCOL_NUM>1)ЃЌDATA_WIDTHашвЊЪЧ8Лђ9ЕФећЪ§БЖЁЃ
                //4. гЩгкВЩгУЭЈгУЕФRAMУшЪіЗНЗЈЃЌЖдPrimitive/LPMЕФФГаЉЬиаджЇГжВЛШЋЃЈвРРЕгкзлКЯЙЄОпЃЉЁЃ
                
                ram_sdp_base_common #
                (
                    .ADDR_WIDTH(ADDR_WIDTH),
                    .DATA_WIDTH(DATA_WIDTH),
                    .REG_OUT   (REG_OUT   ),//0<=REG_OUT<=2. It's highly recommended to set this parama bigger than '0'. Otherwise, synthieser will use logic resource to build the RAM unless the value of MEM_STYLE.
                    .MEM_STYLE (MEM_STYLE ),//For Xilinx : use "AUTO", "BLOCK", or "DISTRIBUTED" ; For Altera : use "logic", "M512", "M4K", "M9K", "M20K", "M144K", "MLAB", or "M-RAM".
                    .USE_RD_EN (USE_RD_EN ),//If USE_RD_EN = 1, user should drive I_rd_en port as read enable. Otherwise, this port is unused, left it un-conected or assign it with any value.
                    .COL_NUM   (COL_NUM   ) //Number of byte write enable bit
                )
                u_ram_sdp_base_common
                (
                    .I_wr_clk (I_wr_clk ),
                    .I_wr_en  (I_wr_en  ),
                    .I_wr_addr(I_wr_addr),
                    .I_wr_data(I_wr_data),
                    .I_rd_clk (I_rd_clk ),
                    .I_rd_en  (I_rd_en  ),
                    .I_rd_addr(I_rd_addr),
                    .O_rd_data(O_rd_data) 
                );
            end
            
    endgenerate
    
endmodule
