// 1. гУДњТыЪЕЯжвЛИіМђЕЅЫЋПкRAM
// 2. жЇГжЪЕЯжRAMЗНЪНЕФПЩВЮЪ§ЛЏЃЌжЇГжAlteraКЭXilinxЛЗОГ
// 3. RAMЕФЪ§ОнПэЖШКЭЕижЗПэЖШПЩВЮЪ§ЛЏ
